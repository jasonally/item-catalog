@mod.route('/fbdisconnect')
def fbdisconnect():
    """Logs out users who are using Facebook account authorization."""
    facebook_id = login_session['facebook_id']
    url = 'https://graph.facebook.com/%s/permissions' % facebook_id
    h = httplib2.Http()
    result = h.request(url, 'DELETE')[1]
    return "You have been logged out"

@mod.route('/logout')
def logout():
    """Univeral logout method which calls Google and Facebook logout methods
    depending on account. Provides easy ability to plug in additional third-
    party authorization methods."""
    if 'provider' in login_session:
        if login_session['provider'] == 'google':
            gdisconnect()
            del login_session['gplus_id']
            del login_session['credentials']
        if login_session['provider'] == 'facebook':
            fbdisconnect()
            del login_session['facebook_id']
        del login_session['username']
        del login_session['email']
        del login_session['picture']
        del login_session['user_id']
        del login_session['provider']
        flash("You have been logged out")
        return redirect(url_for('reading_lists.show_reading_lists'))
    else:
        return redirect(url_for('reading_lists.show_reading_lists'))